package io.igia.caremanagement.repository;

import java.util.List;
import java.util.Optional;

import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.stereotype.Repository;

import io.igia.caremanagement.domain.Episode;


/**
 * Spring Data  repository for the Episode entity.
 */

@Repository
public interface EpisodeRepository extends JpaRepository<Episode, Long> {
    
    List<Episode> findAllByProgramId(Long programId);
    
    Optional<Episode> findOneByLookup(String lookup);
}
